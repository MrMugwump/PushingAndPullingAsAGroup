public class PushAndPull {
    public static void main(String[] args){
        new PushAndPull();
    }
    private PushAndPull(){
        printBen();
        printSpencer();
    }
    private void printBen(){
        System.out.println("My name is Ben Bridenbaugh");
    }
    private void printSpencer(){
        System.out.println("I'm Spencer Barton");
    }
}
